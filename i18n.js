function detectBrowserLang() {
  const lang = (typeof navigator !== 'undefined' && navigator.language) || 'fr';
  const code = lang.toLowerCase().slice(0, 2);
  return I18N_SUPPORTED.includes(code) ? code : 'en';
}

function resolveLang(stored) {
  if (I18N_SUPPORTED.includes(stored)) return stored;
  return detectBrowserLang();
}

function format(str, params) {
  if (!params) return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => (params[k] != null ? params[k] : `{${k}}`));
}

async function getCurrentLang() {
  try {
    const { language } = await chrome.storage.local.get('language');
    return resolveLang(language);
  } catch {
    return detectBrowserLang();
  }
}

function tWith(lang, key, params) {
  const table = I18N_MESSAGES[lang] || I18N_MESSAGES.fr;
  const raw = table[key] ?? I18N_MESSAGES.en[key] ?? I18N_MESSAGES.fr[key] ?? key;
  return format(raw, params);
}

async function t(key, params) {
  const lang = await getCurrentLang();
  return tWith(lang, key, params);
}

function applyDomI18n(root, lang) {
  const scope = root || document;
  scope.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    el.textContent = tWith(lang, key);
  });
  scope.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.getAttribute('data-i18n-placeholder');
    el.setAttribute('placeholder', tWith(lang, key));
  });
  scope.querySelectorAll('[data-i18n-title]').forEach((el) => {
    const key = el.getAttribute('data-i18n-title');
    el.setAttribute('title', tWith(lang, key));
  });
  if (scope.documentElement) scope.documentElement.setAttribute('lang', lang);
}

